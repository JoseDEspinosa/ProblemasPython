# Crear una lista de frutas
frutas = ['manzana', 'banana', 'pera', 'uva']

# Imprimir la lista y su longitud usando len
print("Lista de frutas:", frutas)
print("Longitud de la lista:", len(frutas))

# Eliminar un elemento de la lista usando del
del frutas[1] # Elimina 'banana' de la lista

# Imprimir la lista actualizada
print("Lista de frutas después de eliminar 'banana':", frutas)

# Calcular la longitud de la lista actualizada
longitud_actual = len(frutas)

# Imprimir la longitud actualizada
print("Longitud de la lista actualizada:", longitud_actual)
